#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*str;

	str = ft_itoa(n);
	ft_putstr_fd(str, fd);
	if (ft_strncmp(str, "-2147483648", 12) != 0)
		free(str);
}
